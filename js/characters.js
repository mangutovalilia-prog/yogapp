/* points-строка для polyline/polygon */
function pts(arr) { return arr.map(p => p[0] + ',' + p[1]).join(' '); }

/* Осветлить / затемнить hex-цвет на amt (для мягкого объёма) */
function shade(hex, amt) {
  const n = parseInt(hex.slice(1), 16);
  const clamp = v => Math.max(0, Math.min(255, v));
  const r = clamp((n >> 16) + amt), g = clamp(((n >> 8) & 255) + amt), b = clamp((n & 255) + amt);
  return '#' + ((r << 16) | (g << 8) | b).toString(16).padStart(6, '0');
}

/* Вертикальный градиент (свет сверху) в координатах фигуры */
function vGrad(id, col) {
  return `<linearGradient id="${id}" gradientUnits="userSpaceOnUse" x1="100" y1="0" x2="100" y2="260">
    <stop offset="0" stop-color="${shade(col, 16)}"/><stop offset="1" stop-color="${shade(col, -14)}"/>
  </linearGradient>`;
}

/* Поясок/манжета: короткая линия в точке `to`, перпендикулярная направлению from→to.
   Поворачивается вместе с конечностью в любой позе. */
function perpBand(from, to, half, col, w) {
  const dx = to[0] - from[0], dy = to[1] - from[1];
  const len = Math.hypot(dx, dy) || 1;
  const px = -dy / len * half, py = dx / len * half;
  return `<line x1="${(to[0] - px).toFixed(1)}" y1="${(to[1] - py).toFixed(1)}" x2="${(to[0] + px).toFixed(1)}" y2="${(to[1] + py).toFixed(1)}" stroke="${col}" stroke-width="${w}" stroke-linecap="round"/>`;
}

/* Точка на отрезке neck→pelvis на доле t (для талии/кромки топа) */
function alongSpine(spine, t) {
  const a = spine[0], b = spine[spine.length - 1];
  return [a[0] + (b[0] - a[0]) * t, a[1] + (b[1] - a[1]) * t];
}

