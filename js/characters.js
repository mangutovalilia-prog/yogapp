/* Проработанная фигура — для женщины и зайчихи (та же система суставов) */
function figureSVGDetailed(p, c) {
  const pelvis = p.spine[p.spine.length - 1];
  const [hx, hy, r] = p.head;
  const top = hy - r;
  const armC = c.arm || c.skin, legC = c.legwear || c.stroke, torsoC = c.top || c.stroke;
  const isBunny = c.kind === 'bunny';
  const footRX = isBunny ? 8.5 : 6, footRY = isBunny ? 5 : 4, handR = isBunny ? 5.5 : 4.8;
  const uid = 'f' + Math.random().toString(36).slice(2, 7);
  const skinG = `url(#${uid}-skin)`;

  const line = (a, col, w) => `<polyline points="${pts(a)}" fill="none" stroke="${col}" stroke-width="${w}" stroke-linecap="round" stroke-linejoin="round"/>`;
  const hand = pt => `<circle cx="${pt[0]}" cy="${pt[1]}" r="${handR}" fill="${skinG}" stroke="${c.stroke}" stroke-width="1"/>`;
  const foot = pt => `<ellipse cx="${pt[0]}" cy="${pt[1] + 1}" rx="${footRX}" ry="${footRY}" fill="${skinG}" stroke="${c.stroke}" stroke-width="1"/>`;

  let ears = '', tail = '';
  if (isBunny) {
    ears = `<g stroke="${c.stroke}" stroke-width="1.5" fill="${c.skin}">
        <ellipse cx="${hx - 7}" cy="${top - 13}" rx="5.5" ry="17" transform="rotate(-13 ${hx - 7} ${top - 13})"/>
        <ellipse cx="${hx + 7}" cy="${top - 13}" rx="5.5" ry="17" transform="rotate(13 ${hx + 7} ${top - 13})"/></g>
      <ellipse cx="${hx - 7}" cy="${top - 11}" rx="2.4" ry="10.5" fill="${c.hair}" opacity=".7" transform="rotate(-13 ${hx - 7} ${top - 11})"/>
      <ellipse cx="${hx + 7}" cy="${top - 11}" rx="2.4" ry="10.5" fill="${c.hair}" opacity=".7" transform="rotate(13 ${hx + 7} ${top - 11})"/>`;
    tail = `<circle cx="${pelvis[0] + 12}" cy="${pelvis[1]}" r="8" fill="#fffaf6" stroke="${c.stroke}" stroke-width="1.5"/>`;
  }
  const isWoman = c.kind === 'human-f';
  // причёска: объём за головой + аккуратный пучок с прядями у лица
  const hair = isWoman
    ? `<ellipse cx="${hx}" cy="${hy - 1.5}" rx="${r + 3}" ry="${r + 2.5}" fill="${c.hair}"/>`
    : '';
  const hairFront = isWoman
    ? `<path d="M${hx - r + 0.5} ${hy - r + 4} q -3.5 ${r * 0.7} -0.5 ${r + 3}" fill="none" stroke="${c.hair}" stroke-width="2.4" stroke-linecap="round"/>
       <path d="M${hx + r - 0.5} ${hy - r + 4} q 3.5 ${r * 0.7} 0.5 ${r + 3}" fill="none" stroke="${c.hair}" stroke-width="2.4" stroke-linecap="round"/>`
    : '';
  const bun = isWoman
    ? `<ellipse cx="${hx}" cy="${top - 2.5}" rx="7.5" ry="7" fill="${c.hair}"/>
       <ellipse cx="${hx - 2}" cy="${top - 4}" rx="3" ry="2.6" fill="${shade(c.hair, 22)}" opacity=".4"/>
       <path d="M${hx - 6} ${top + 2.5} q 6 4 12 0" fill="none" stroke="${shade(c.hair, -22)}" stroke-width="1.8" stroke-linecap="round"/>`
    : '';

  // одежда женщины: спортивный топ + открытая талия + пояс и манжеты леггинсов
  const neck = p.spine[0];
  const mid = alongSpine(p.spine, 0.62);
  const torsoMarkup = isWoman
    ? line([neck, mid], `url(#${uid}-torso)`, 20) + line([mid, pelvis], skinG, 16)
    : line(p.spine, `url(#${uid}-torso)`, 20);
  const topHem = isWoman ? perpBand(neck, mid, 10.5, shade(torsoC, -20), 2.4) : '';
  const waistband = isWoman ? perpBand(mid, pelvis, 10, shade(legC, -22), 4.5) : '';
  const cuffs = isWoman
    ? perpBand(p.legL[1], p.legL[2], 7, shade(legC, -14), 3.4) + perpBand(p.legR[1], p.legR[2], 7, shade(legC, -14), 3.4)
    : '';

  const defs = `<defs>
    ${vGrad(uid + '-arm', armC)}${vGrad(uid + '-leg', legC)}${vGrad(uid + '-torso', torsoC)}${vGrad(uid + '-skin', c.skin)}
    <radialGradient id="${uid}-sh" cx="0.5" cy="0.5" r="0.5">
      <stop offset="0" stop-color="#000" stop-opacity=".15"/><stop offset="1" stop-color="#000" stop-opacity="0"/>
    </radialGradient>
  </defs>`;
  // мягкая подсветка суставов (колени/локти) — еле заметный объём
  const joint = (pt, w) => `<circle cx="${pt[0]}" cy="${pt[1]}" r="${w / 2 - 0.5}" fill="${shade(c.skin, 18)}" opacity=".28"/>`;

  return `<svg viewBox="0 0 200 260" xmlns="http://www.w3.org/2000/svg" role="img">
    ${defs}
    <ellipse cx="100" cy="250" rx="78" ry="11" fill="url(#${uid}-sh)"/>
    ${ears}${tail}
    ${line(p.legL, `url(#${uid}-leg)`, 13)}${line(p.legR, `url(#${uid}-leg)`, 13)}
    ${cuffs}
    ${joint(p.legL[1], 13)}${joint(p.legR[1], 13)}
    ${foot(p.legL[2])}${foot(p.legR[2])}
    ${torsoMarkup}
    ${topHem}${waistband}
    ${line(p.armL, `url(#${uid}-arm)`, 11)}${line(p.armR, `url(#${uid}-arm)`, 11)}
    ${joint(p.armL[1], 11)}${joint(p.armR[1], 11)}
    ${hand(p.armL[2])}${hand(p.armR[2])}
    ${hair}
    <circle cx="${hx}" cy="${hy}" r="${r}" fill="url(#${uid}-skin)" stroke="${c.stroke}" stroke-width="2"/>
    ${hairFront}${bun}${faceSVG(c, p.head)}
  </svg>`;
}

/* Отрисовка SVG из координат суставов (p) для персонажа (c) */
function figureSVG(p, c) {
  return c.detailed ? figureSVGDetailed(p, c) : figureSVGSimple(p, c);
}

/* SVG фигуры в позе для выбранного персонажа */
function buildFigure(poseKey, charKey) {
  return figureSVG(POSES[poseKey] || POSES.mountain, CHARACTERS[charKey] || CHARACTERS.woman);
}

/* Линейная интерполяция между двумя позами (для анимации входа в асану) */
function lerpPts(A, B, t) { return A.map((p, i) => p.map((v, j) => v + (B[i][j] - v) * t)); }
function lerpPose(A, B, t) {
  return {
    head:  A.head.map((v, i) => v + (B.head[i] - v) * t),
    spine: lerpPts(A.spine, B.spine, t),
    armL:  lerpPts(A.armL, B.armL, t), armR: lerpPts(A.armR, B.armR, t),
    legL:  lerpPts(A.legL, B.legL, t), legR: lerpPts(A.legR, B.legR, t),
  };
}

/* Плавно «войти» в позу из положения стоя, затем мягко «дышать» (CSS) */
function animateFigure(el, poseKey, charKey) {
  const target = POSES[poseKey] || POSES.mountain;
  const start = POSES.mountain;
  const c = CHARACTERS[charKey] || CHARACTERS.woman;
  el.classList.remove('breathing');
  const settle = () => { el.innerHTML = figureSVG(target, c); el.classList.add('breathing'); };
  if (poseKey === 'mountain') { settle(); return; }

  const dur = 1000; let t0 = null, done = false;
  const finishOnce = () => { if (!done) { done = true; settle(); } };
  function frame(now) {
    if (done) return;
    if (!t0) t0 = now;
    const t = Math.min(1, (now - t0) / dur);
    const e = t < 0.5 ? 2 * t * t : 1 - Math.pow(-2 * t + 2, 2) / 2; // easeInOutQuad
    el.innerHTML = figureSVG(lerpPose(start, target, e), c);
    if (t < 1) requestAnimationFrame(frame); else finishOnce();
  }
  requestAnimationFrame(frame);
  // гарантия финальной позы, даже если requestAnimationFrame тормозит (фон/неактивная вкладка)
  setTimeout(finishOnce, dur + 250);
}

